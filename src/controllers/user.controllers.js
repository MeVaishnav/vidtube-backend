const generateAccessAndRefreshToken = async (userId) => {
  try {
    const user = User.findById(userId);

    if (!userId) {
      throw new ApiError(400, "user not found");
    }
    const accessToken = user.genearteAccessToken();
    const refreshToken = user.genearteRefreshToken();

    user.refreshToken = refreshToken;
    await user.save({ validateBeforeSave: false });
    return { accessToken, refreshToken };
  } catch (error) {
    throw new ApiError(
      500,
      "something went wrong while generating access and refresh tokens"
    );
  }
};

const registerUser = asyncHandler(async (req, res) => {
  const { fullname, email, username, password } = req.body;

  // Validate required fields
  if (
    [fullname, email, username, password].some(
      (f) => !f || String(f).trim() === ""
    )
  ) {
    throw new ApiError(400, "All fields are required");
  }

  // Check if user exists
  const existedUser = await User.findOne({ $or: [{ username }, { email }] });
  if (existedUser) {
    throw new ApiError(
      409,
      "User with the same email or username already exists"
    );
  }

  // Get uploaded file paths
  const avatarLocalPath = req.files?.avatar?.[0]?.path;
  const coverLocalPath = req.files?.coverImage?.[0]?.path;

  console.log("Avatar local path:", avatarLocalPath);
  console.log("Cover local path:", coverLocalPath);

  if (!avatarLocalPath) {
    throw new ApiError(
      400,
      "Avatar file is missing. Make sure frontend sends file as 'avatar'"
    );
  }

  // Upload avatar to Cloudinary
  let avatarUpload;
  try {
    avatarUpload = await uploadOnCloudinary(avatarLocalPath);
  } catch (err) {
    console.error("Cloudinary upload error:", err);
    throw new ApiError(
      500,
      "Failed to upload avatar. Check Cloudinary credentials and file path."
    );
  }

  if (!avatarUpload) {
    throw new ApiError(
      500,
      "Failed to upload avatar. Cloudinary returned null"
    );
  }

  // Upload cover image if provided
  let coverUpload = null;
  if (coverLocalPath) {
    try {
      coverUpload = await uploadOnCloudinary(coverLocalPath);
    } catch (err) {
      console.error("Cloudinary cover upload error:", err);
      coverUpload = null; // Not critical, proceed without cover
    }
  }

  // Create new user
  try {
    const newUser = await User.create({
      fullname: fullname,
      avatar: avatarUpload.secure_url,
      coverImage: coverUpload?.secure_url || "",
      email,
      password,
      username: username.toLowerCase(),
    });

    // Exclude sensitive fields
    const createdUser = await User.findById(newUser._id).select(
      "-password -refreshToken"
    );
    if (!createdUser) {
      throw new ApiError(
        500,
        "Something went wrong while registering the user"
      );
    }

    return res
      .status(201)
      .json(new ApiResponse(201, createdUser, "User registered successfully"));
  } catch (error) {
    console.log("user creation failed");

    if (avatar) {
      await deleteFromCloudinary(avatar.public_id);
    }
    if (coverImage) {
      await deleteFromCloudinary(coverImage.public_id);
    }

    throw new ApiError(
      500,
      "Something went wrong while registering the user and images were deleted"
    );
  }
});

const loginUser = asyncHandler(async (req, res) => {
  const { email, username, password } = req.body;

  if (!email) {
    throw new ApiError(400, "Email is required");
  } else if (!username) {
    throw new ApiError(400, "username is required");
  } else if (!password) {
    throw new ApiError(400, "password is required");
  }

  const user = await User.findOne({
    $or: [{ username }, { email }],
  });

  if (!user) {
    throw new ApiError(404, "user not found");
  }

  const isPasswordValid = await user.isPasswordCorrect(password);

  if (!isPasswordValid) {
    throw new ApiError(401, "Invalid user credentials");
  }

  const { accessToken, refreshToken } = await generateAccessAndRefreshToken(
    user._id
  );

  const loggedInUser = await User.findById(user._id).select(
    "-password -refreshToken "
  );

  if (!loggedInUser) {
    throw new ApiError(500, "Something went wrong while fetching the user");
  }

  const options = {
    httpOnly: true,
    secure: process.env.NODE_ENV === "production",
  };

  return res
    .status(200)
    .cookie("accessToken", accessToken, options)
    .cookie("refreshToken", refreshToken, options)
    .json(
      new ApiResponse(
        200,
        { user: loggedInUser, accessToken, refreshToken },
        "User logged in safely"
      )
    );
});

const refreshAccessToken = asyncHandler(async (erq, res) => {
  const incomingRefreshToken =
    req.cookies.refreshToken || req.body.refreshToken;

  if (!incomingRefreshToken) {
    throw new ApiError(401, "Refresh token is required");
  }

  try {
    const decodedToken = jwt.verify(
      incomingRefreshToken,
      process.env.REFRESH_TOKEN_SECRET
    );

    const user = await User.findById(decodedToken?._id);

    if (!user) {
      throw new ApiError(401, "Invalid refresh token");
    }

    if (incomingRefreshToken !== user?.refreshToken) {
      throw new ApiError(401, "Invalid refresh token");
    }

    const options = {
      httpOnly: true,
      secure: process.env.NODE_ENV === "production",
    };

    const { accessToken, refreshToken: newRefreshToken } =
      await generateAccessAndRefreshToken(user._id);

    return res
      .status(200)
      .cookie("accessToken", accessToken, options)
      .cookie("refreshToken", newRefreshToken, options)
      .json(
        new ApiResponse(
          200,
          { accessToken, refreshToken: newRefreshToken },
          "Acccess token refreshed succesfully"
        )
      );
  } catch (error) {
    throw new ApiError(
      500,
      "Something went wrong while refreshing the access token"
    );
  }
});

